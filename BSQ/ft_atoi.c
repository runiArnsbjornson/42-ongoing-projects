#include "bsq.h"

int		ft_atoi(char *str)
{
	int c;
	int n;
	int r;

	c = 0;
	n = 1;
	r = 0;
	while (str[c] == '\n' || str[c] == '\r' || str[c] == '\f'
		|| str[c] == '\v' || str[c] == '\t' || str[c] == ' ')
		c++;
	if (str[c] == '-' || str[c] == '+')
	{
		if (str[c] == '-')
			n = -1;
		c++;
	}
	while (str[c] != '\0')
	{
		if (str[c] < '0' || str[c] > '9')
			return (r * n);
		r = (r * 10 + (str[c]) - '0');
		c++;
	}
	return (r * n);
}
